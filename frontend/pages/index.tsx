import Head from 'next/head'
import { useState, useEffect } from 'react';
import Navbar from '../components/Navbar';
import LoginModal from '../components/LoginModal';
import RegistrationModal from '../components/RegistrationModal';
import styles from '../styles/Home.module.css'
import useStore from '../store';

import { User, API_BASE_URL } from '../data';

const Home: React.FC = () => {
  const [showLogin, setShowLogin] = useState(false);
  const [showRegistration, setShowRegistration] = useState(false);
  const [user, setUser] = useState<User | null>(null);

  let store: any = useStore();

  useEffect(() => {
    if(store.sessionToken !== null) {
      fetchUser();
    }
  }, [store.sessionToken])

  const logOut = async(token: string | null) => {
    if(token !== null) {
      await fetch(`${API_BASE_URL}/logout`, {
          headers: {
            'Authorization': token,
          }
      })
      store.setToken(null);
      setUser(null);
    }
  }

  const fetchUser = async () => {
    if(store.sessionToken !== null) {
      fetch(`${API_BASE_URL}/user`, {
        headers: {
          'Authorization': store.sessionToken,
        }
      }).then(res => res.json()).then(res => setUser(JSON.parse(res)));
    }
  }

  let main = (
    <main className={styles.main}>
      <h1>Hello { user ? user.name : null}</h1>
      <button onClick={() => { setShowLogin(!showLogin) }}>Login</button>
      <button onClick={() => { setShowRegistration(!showRegistration) }}>Register</button>

      <div id="modal-root" />

      <RegistrationModal
        onClose={() => setShowRegistration(false) }
        onSuccess={() => {
          console.log("sess", store);
          setShowRegistration(false);
          fetchUser()
        }}
        show={showRegistration}
      />

      <LoginModal
        onClose={() => setShowLogin(false) }
        onSuccess={async() => {
          await fetchUser()
          setShowLogin(false);
        }}
        show={showLogin}
      />
    </main>
  );


  return (
    <div className={styles.container}>
      <Head>
        <title>Workout Tracker</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Navbar
        onLogout={() => logOut(store.sessionToken) }
        onLogin={() => setShowLogin(true) }
        onRegister={() => setShowRegistration(true) }
      />

      { main }

    </div>
  )
}

export default Home;
